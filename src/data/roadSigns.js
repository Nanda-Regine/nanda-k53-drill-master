// ── Helper functions ─────────────────────────────────────────────────────────────
export function getSignsByCategory(category) {
  return ROAD_SIGNS.filter(s => s.category === category);
}

export function getRandomSigns(count = 10) {
  return [...ROAD_SIGNS].sort(() => Math.random() - 0.5).slice(0, count);
}

export function getSignById(id) {
  return ROAD_SIGNS.find(s => s.id === id);
}

export function getConfusablePairs() {
  return CONFUSABLE_PAIRS.map(([a, b]) => ({
    signA: getSignById(a),
    signB: getSignById(b),
  })).filter(p => p.signA && p.signB);
}

export function getSignsByShape(shape) {
  return ROAD_SIGNS.filter(s => s.shape === shape);
}
